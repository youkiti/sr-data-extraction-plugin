export type MethodsLanguage = 'en' | 'ja';
export type MethodsWorkflow = 'single' | 'dual';

/** プレースホルダへ自動反映する実績値。null / 空配列 / 0 = 反映できず {{...}} のまま残す */
export interface MethodsFacts {
  toolVersion: string | null;
  /** run_type=full の完了 run の model_version（新しい順・重複除去）。複数は「, 」区切りで列挙 */
  modelIds: string[];
  /** 同 run の provider 表示名（gemini → Gemini / openrouter → OpenRouter。重複除去） */
  providers: string[];
  /** run_type=pilot の完了 run の対象 study 数（和集合の要素数）。0 なら反映しない（{{n_pilot}} のまま） */
  pilotStudyCount: number;
  /** text_status = no_text_layer の document 数（0 ならオプション文自体を出さない） */
  scannedDocumentCount: number;
}

export interface BuiltMethodsText {
  /** コピー用プレーンテキスト（見出しは「Data extraction.」/「データ抽出.」をプレーンで先頭に付ける。§4 準拠） */
  text: string;
  /** 未反映のまま残ったプレースホルダ名（重複除去・出現順。注意書き表示の素材） */
  unresolved: string[];
}


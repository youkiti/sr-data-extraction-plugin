/** text 中の {{key}} をすべて value に置換する（value が null なら手を付けず {{key}} を残す） */
function applyPlaceholder(text: string, key: string, value: string | null): string {
  if (value === null) {
    return text;
  }
  return text.split(`{{${key}}}`).join(value);
}

/** 残存する {{...}} プレースホルダ名を出現順・重複除去で列挙する */
function findUnresolvedPlaceholders(text: string): string[] {
  const tokens = text.match(/\{\{[a-zA-Z0-9_]+\}\}/g);
  /* istanbul ignore if -- 正典の文案（§1〜2）は n_sample / reviewer_initials / supplement_ref を
     必ず自動反映しないため、buildMethodsText 経由では未反映 0 件（= match が null）にはならない防御分岐 */
  if (tokens === null) {
    return [];
  }
  const names = tokens.map((token) => token.slice(2, -2));
  return Array.from(new Set(names)); // Set はキー追加順を保持するため出現順のまま重複除去できる
}

/**
 * 論文 Methods セクションの記載例を組み立てる（docs/methods-boilerplate.md §1〜2 の正典どおり）。
 * 自動反映できる実績値（tool_version / model_id / provider / n_pilot / n_scanned）はプレースホルダへ
 * 埋め込み、それ以外（n_sample / reviewer_initials / adjudicator_initials / supplement_ref）は
 * 常に {{...}} のまま残す（§3: ユーザー自身の編集を促す）
 */
export function buildMethodsText(
  language: MethodsLanguage,
  workflow: MethodsWorkflow,
  facts: MethodsFacts,
): BuiltMethodsText {
  const paragraph2 = workflow === 'dual' ? PARAGRAPH_2_DUAL[language] : PARAGRAPH_2_SINGLE[language];
  const parts = [`${HEADING[language]} ${PARAGRAPH_1[language]}`, paragraph2];
  if (facts.scannedDocumentCount > 0) {
    parts.push(OPTIONAL_SCANNED[language]);
  }
  let text = parts.join('\n\n');
  text = applyPlaceholder(text, 'tool_version', facts.toolVersion);
  text = applyPlaceholder(
    text,
    'model_id',
    facts.modelIds.length > 0 ? facts.modelIds.join(', ') : null,
  );
  text = applyPlaceholder(
    text,
    'provider',
    facts.providers.length > 0 ? facts.providers.join(', ') : null,
  );
  text = applyPlaceholder(
    text,
    'n_pilot',
    facts.pilotStudyCount > 0 ? String(facts.pilotStudyCount) : null,
  );
  text = applyPlaceholder(
    text,
    'n_scanned',
    facts.scannedDocumentCount > 0 ? String(facts.scannedDocumentCount) : null,
  );
  // n_sample / reviewer_initials / adjudicator_initials / supplement_ref は常に自動反映しない（§3）
  return { text, unresolved: findUnresolvedPlaceholders(text) };
}
